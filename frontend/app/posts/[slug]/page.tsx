export default async function Post({ params }: { params: { slug: string } }) {
  const post = posts[params.slug as keyof typeof posts]

  if (!post) {
    return (
      <div className="min-h-screen bg-white flex items-center justify-center">
        <div className="text-center">
          <h1 className="text-2xl font-light text-gray-900 mb-4">Post not found</h1>
          <Link href="/" className="text-gray-600 hover:text-gray-900 transition-colors">
            ← Back to blog
          </Link>
        </div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-white">
      <header className="border-b border-gray-100">
        <div className="max-w-2xl mx-auto px-6 py-8">
          <Link href="/" className="inline-flex items-center text-gray-600 hover:text-gray-900 transition-colors">
            <ArrowLeft className="w-4 h-4 mr-2" />
            Back to blog
          </Link>
        </div>
      </header>

      <main className="max-w-2xl mx-auto px-6 py-12">
        <article>
          <header className="mb-12">
            <time className="text-sm text-gray-500 font-mono">
              {new Date(post.date).toLocaleDateString("en-US", {
                year: "numeric",
                month: "long",
                day: "numeric",
              })}
            </time>
            <h1 className="text-3xl font-light text-gray-900 mt-4 leading-tight">{post.title}</h1>
          </header>

          <div className="prose prose-gray max-w-none">
            {post.content.split("\n\n").map((paragraph, index) => {
              if (paragraph.startsWith("## ")) {
                return (
                  <h2 key={index} className="text-xl font-light text-gray-900 mt-12 mb-6">
                    {paragraph.replace("## ", "")}
                  </h2>
                )
              }
              if (paragraph.startsWith("### ")) {
                return (
                  <h3 key={index} className="text-lg font-light text-gray-900 mt-8 mb-4">
                    {paragraph.replace("### ", "")}
                  </h3>
                )
              }
              if (paragraph.startsWith("- ")) {
                const items = paragraph.split("\n").filter((line) => line.startsWith("- "))
                return (
                  <ul key={index} className="space-y-2 my-6">
                    {items.map((item, itemIndex) => (
                      <li key={itemIndex} className="text-gray-700 leading-relaxed">
                        {item.replace("- ", "")}
                      </li>
                    ))}
                  </ul>
                )
              }
              if (paragraph.match(/^\d+\./)) {
                const items = paragraph.split("\n").filter((line) => line.match(/^\d+\./))
                return (
                  <ol key={index} className="space-y-2 my-6">
                    {items.map((item, itemIndex) => (
                      <li key={itemIndex} className="text-gray-700 leading-relaxed">
                        {item.replace(/^\d+\.\s/, "")}
                      </li>
                    ))}
                  </ol>
                )
              }
              if (paragraph.startsWith("**") && paragraph.endsWith("**")) {
                return (
                  <p key={index} className="text-gray-700 leading-relaxed my-6">
                    <strong className="font-medium text-gray-900">{paragraph.replace(/\*\*/g, "")}</strong>
                  </p>
                )
              }
              if (paragraph.trim()) {
                return (
                  <p key={index} className="text-gray-700 leading-relaxed my-6">
                    {paragraph.split("**").map((part, partIndex) =>
                      partIndex % 2 === 1 ? (
                        <strong key={partIndex} className="font-medium text-gray-900">
                          {part}
                        </strong>
                      ) : (
                        part
                      ),
                    )}
                  </p>
                )
              }
              return null
            })}
          </div>
        </article>
      </main>

      <footer className="border-t border-gray-100 mt-24">
        <div className="max-w-2xl mx-auto px-6 py-8">
          <p className="text-sm text-gray-500">© 2024 Blog. All rights reserved.</p>
        </div>
      </footer>
    </div>
  )
}
